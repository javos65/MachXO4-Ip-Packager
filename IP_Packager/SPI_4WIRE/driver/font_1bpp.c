// draw text string, 4bbp font, capitals only
void draw_text_ssd1306( const char *text, font1bpp myfont, uint8_t col, uint8_t page,uint16_t color1)
{
char c=0;
uint16_t wcomp,bytew,width,ip,cc,height = myfont.height;
uint8_t  *fmap= (uint8_t*) myfont.fontmap;
uint16_t charcursor=0;

cc=0;
//printf("Printing [%s]\n\r",text);
while( (c=text[cc]) !=0 ){
 //if(c>myfont.last) c=c-32; // change small to large ascii
 if ( (c>=myfont.first) && (c<=myfont.last)){
      ip =  (c-myfont.first)*FIXEDFONT_WIDTH ;  // pointer to first byte
      width= FIXEDFONT_WIDTH;        			// number of pixels wide
      //printf(" *Char [%c] w*h=%d*%d",c,width,height);
      //printf(" * Character: %c points to index %d\n\r",c,ip);
      draw_font_ssd1306( (uint8_t *) (fmap + ip),(uint8_t) col+charcursor,(uint8_t) page,(uint8_t) width, (uint8_t) height, color1) ;
      charcursor+= (width+1);
  }
 cc++; if(cc>36) break; // escape in case there is no ending 0
 }
}

// draw one letter of font - using Global font_buffer
void draw_font_ssd1306(uint8_t *pSrc, uint8_t col, uint8_t page,uint8_t xSize, uint8_t ySize,uint16_t color)
{
	uint8_t fpixel;
	oled_setblock_ssd1306(col,page,xSize+1, ySize/8); // define writing block on oled x,y ,w, heigt in page
	for( uint8_t c=0;c<xSize;c++)
			for (uint8_t r=0;r<ySize/8;r++) 	// 1bpp = 8 pixels per byte
			{
			//compute color code1 - fpixel lower 4 bits
			fpixel = *(pSrc+ c + (r*xSize/2));
			if (color == TEXT_INVERSE) fpixel= ~fpixel; // inverted pixels
			data_ssd1306( fpixel );
			}
			if (color == TEXT_INVERSE) fpixel= 0xff; // inverted pixels
			else fpixel = 0x00;
			data_ssd1306( fpixel );

}

