uint32_t SPI_BASE; 		// Base Memory Mapped address

// set SPI base address - check sys_platform.h
void set_base_ssd1306(uint32_t address)
{
	SPI_BASE = address;
}

// set SPI base address - check sys_platform.h
uint32_t get_base_ssd1306()
{
	return(SPI_BASE);
}

// set SPI base address - check sys_platform.h
uint32_t get_id_ssd1306()
{
	return(readSPIMemory_ssd1306(ID_REGISTER));
}

uint8_t wait_spi_ssd1306()
 {
	uint32_t x;
	for(uint8_t t=0;t<16;++t){
		x=*( (uint32_t*) (SPI_BASE + STATUS_REGISTER ) );
		//printf("* spi status 0x%08x\n",x);
		if ( !(x & 0x00010000) ) return(1); // check not FULL : bit[16]
		delay(1);
	}
	//printf("* spi fifo full : timeout\n");
	return(0);
 }

uint8_t command_ssd1306(uint8_t command)
{
	if (wait_spi_ssd1306() )
	{
		*( (uint32_t*) (SPI_BASE + DATA_REGISTER)) = 0x00000000 + command;
		*( (uint32_t*) (SPI_BASE + CONTROL_REGISTER)) = 0x00000001;
		//printf("C[0x%02X],",command);
		return(1);
	}
	return(0); // failure
}


uint8_t data_ssd1306(uint8_t data)
{
	if (wait_spi_ssd1306() )
	{
		*( (uint32_t*) (SPI_BASE + DATA_REGISTER)) = 0x00000100 + data;
		*( (uint32_t*) (SPI_BASE + CONTROL_REGISTER)) = 0x00000001;
		//printf("D[0x%02X],",data);
		return(1);
	}
	else return(0); // failure
}


void writeSPIMemory_ssd1306(uint8_t offset,uint32_t data)
{
*( (uint32_t*) (SPI_BASE + offset)) = data;
}


uint32_t readSPIMemory_ssd1306(uint8_t offset)
{
return ( *( (uint32_t*) (SPI_BASE + offset) ) );
}


void  commandlist_ssd1306(const uint8_t* list,uint8_t size) {
  for (uint8_t t=0;t<size;++t)
	  {command_ssd1306(list[t]);}
 }


// Fill the oled with a pattern : 128x32 bits ,in 512 bytes/
void oled_clear_ssd1306(uint8_t pattern)
{
	oled_graph[1]=0;oled_graph[2]=127;oled_graph[4]=0;oled_graph[5]=4; 	// put command into indexed array Col 0-127, Pag 0-4
	commandlist_ssd1306(oled_graph,OLED_GRAPH_SIZE); 					// set block to full screen
	command_ssd1306(SSD1306_SETSTARTLINE |0x00);	 					// set start to 0x00
	for(uint16_t i=0;i<SSD1306_D;++i) {data_ssd1306(pattern);}  		// send data pattern
}


//Send lattice logo bmp data place at col/page location - to be replaced by an image structure
void oled_lattice_ssd1306(uint8_t column,uint8_t page,uint8_t type)
{
	oled_graph[1]= column;oled_graph[2]=column+LATTICE_WIDTH-1;
	oled_graph[4]=page;oled_graph[5]=page+(LATTICE_HEIGHT/8)+1;			// put data into indexed setup array
	commandlist_ssd1306(oled_graph,OLED_GRAPH_SIZE); 					// set block to full screen
	//command_ssd1306(SSD1306_SETSTARTLINE |0x00);	 					// set start to 0x00
	for(uint16_t i=0;i<LATTICE_SIZE;++i) {
		if (type== 1 ) data_ssd1306( lattice_logo[i] );
		else data_ssd1306( ~lattice_logo[i] );
	}  		// send data pattern
}

void oled_setblock_ssd1306(uint8_t column,uint8_t page,uint8_t width, uint8_t height)
{
	oled_graph[1]= column;oled_graph[2]=column+width-1;
	oled_graph[4]=page;oled_graph[5]=page+height-1;			// put data into indexed setup array
	commandlist_ssd1306(oled_graph,OLED_GRAPH_SIZE); 		// set block to full screen
	command_ssd1306(SSD1306_SETSTARTLINE |0x00);			// set startline to zero
}

